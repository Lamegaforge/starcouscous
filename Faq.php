<p>Définitions</p>

Dans les présentes CGV, les mots ou expressions suivants auront les significations suivantes :<br>
- Les termes le « Vendeur » et le « Restaurant » désignent la société d’exploitation du Restaurant sous enseigne Starcouscous sélectionné par le Client et auprès de laquelle la commande sera enregistrée et dont les coordonnées seront indiquées sur l’email confirmant la commande et sur le ticket de caisse délivré dans le Restaurant participant lors du retrait ou de la livraison des Produits.<br>
- Le terme « Commande » désigne le service, mis à disposition par l’Editeur, et l’ensemble du processus de commande en ligne de produits disponibles dans les Restaurants participants. Le Client effectue le paiement de sa commande en ligne, à la livraison ou au moment du retrait des produits<br>

 

<p>1.    Acceptation des CGV</p>

Le Client déclare avoir pris connaissance et accepté les présentes CGV avant la passation de sa Commande. Toute Commande d’un Produit sur le Site matérialise donc de la part du Client l’acceptation expresse des présentes CGV.<br>

 

<p>2.    Les Produits</p>

Les produits proposés par le Restaurant choisi par le Client sont ceux qui figurent dans le catalogue publié sur le Site le jour de la Commande, dans la limite toutefois des stocks disponibles. Il est recommandé de consommer rapidement les produits à compter de leur retrait en Restaurant et de ne jamais les congeler.<br>

Les produits présentés à la vente sont susceptibles d’être modifiés ou supprimés par le Vendeur sans aucun préavis. Les photographies et illustrations présentées en ligne ne sont pas contractuelles.<br>

 

<p>3.    Création d’un compte</p>

Pour commander, le Client doit créer un compte qui lui permettra notamment d’accéder à l’historique de ses Commandes.<br>

Lors de son inscription, le Client choisit un identifiant et un mot de passe auxquels il associe ses nom / prénoms, date de naissance, une adresse précise (numéro de bâtiment, d’étage, digicode) et un numéro de téléphone opérationnel afin de permettre au livreur du restaurant Starcouscous de sa zone géographique de livrer la commande dans les meilleurs conditions et délais.<br>

Il appartient au Client de garder ses identifiant et un mot de passe strictement confidentiels et de ne pas les communiquer à des tiers, afin d’éviter autant que possible tout risque d’intrusion de son compte Client et la passation de Commandes à son insu par des personnes non autorisées. Le Vendeur ne saurait être tenu responsable de toute utilisation du compte du Client par un tiers qui aurait eu accès à son identifiant et à son mot de passe de quelque manière que ce soit.<br>

 

<p>4.    Passation des Commandes</p>

    Se rendre sur le Site http://starcouscous.bureau713.fr/,<br>
    Créer un compte ou se connecter sur son compte avec son identifiant et son mot de passe<br>
    Sélectionner le Restaurant participant au service de commande en ligne.<br>
    Effectuer sa Commande en sélectionnant les Produits,<br>
    Valider sa Commande après avoir vérifié le récapitulatif de sa Commande,<br>
    Accepter les CGV,<br>
    Effectuer éventuellement en ligne le paiement de sa Commande<br>
    Choisir d’être livré ou se rendre au Restaurant participant choisi muni impérativement de sa confirmation de Commande envoyée par courrier électronique.<br>

La validation finale de la Commande entrainent acceptation par le Client des présentes CGV, la reconnaissance d’en avoir parfaite connaissance et la renonciation à se prévaloir de ses propres conditions d’achat ou de toutes autres conditions.<br>

S’il décide de choisir le service de retrait de sa commande, il appartient au Client de vérifier les heures de fermeture du Restaurant sur la page du Restaurant qu’il aura sélectionné afin d’opérer ledit retrait pendant les heures d’ouverture du restaurant sélectionné.<br>

 

<p>5.    Preuve de la Commande</p>

La validation finale de la Commande vaudra preuve de l’intégralité de ladite Commande et vaudra exigibilité des sommes engagées par la Commande.<br>

