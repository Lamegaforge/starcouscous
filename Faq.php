<p>6.    Annulation et modification d’une Commande</p>

Selon les dispositions de l’article L121-20-2 3° du Code de la Consommation, le droit de rétractation applicable en matière de vente à distance ne peut être exercé dans le cas de la fourniture de biens qui du fait de leur nature sont susceptibles de se détériorer ou de se périmer rapidement.<br>

En application de ce texte, il est expressément indiqué que toute Commande sur le Site est ferme et définitive et que l’exercice du droit de rétractation est exclu.<br>

Les Commandes qui ont donc été définitivement validées ne sont pas annulables. Aucune marchandise ne peut être reprise ou échangée.<br>

 

<p>7.    Retrait ou livraison des Produits</p>

